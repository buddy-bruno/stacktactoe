// Supabase Edge Function: Matchmaking (optional)
// Die Next.js-Lobby ruft die RPC join_matchmaking_queue direkt auf; diese Edge Function
// ist ein optionaler Proxy (z. B. für externe Clients oder serverseitige Aufrufe).
// Deploy: supabase functions deploy matchmaking
// Aufruf: POST .../functions/v1/matchmaking mit { "queue_type": "casual" | "ranked" }
// Gibt zurück: { "game_id": "uuid" } oder { "game_id": null, "status": "waiting" }

import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

Deno.serve(async (req) => {
  if (req.method === "OPTIONS") return new Response("ok", { headers: corsHeaders });

  try {
    const authHeader = req.headers.get("Authorization");
    if (!authHeader) {
      return new Response(
        JSON.stringify({ error: "Unauthorized" }),
        { status: 401, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    const supabase = createClient(
      Deno.env.get("SUPABASE_URL") ?? "",
      Deno.env.get("SUPABASE_ANON_KEY") ?? "",
      { global: { headers: { Authorization: authHeader } } }
    );

    const { data: { user } } = await supabase.auth.getUser();
    if (!user) {
      return new Response(
        JSON.stringify({ error: "Unauthorized" }),
        { status: 401, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    const body = await req.json().catch(() => ({}));
    const queueType = body?.queue_type ?? "casual";

    const { data: gameId, error } = await supabase.rpc("join_matchmaking_queue", {
      p_queue_type: queueType,
    });

    if (error) {
      return new Response(
        JSON.stringify({ error: error.message }),
        { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(
      JSON.stringify({ game_id: gameId, status: gameId ? "matched" : "waiting" }),
      { headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  } catch (e) {
    return new Response(
      JSON.stringify({ error: String(e) }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
